var arr= [1,2,3,4,5];  // 'a' is a Array
// 1 is at 0th index. And 5 is at 4th index
// push pop shift unshift
arr.push(6); // adds value to the last number
arr.pop(); // deletes last value in array
arr.unshift(0); // to add 1st value in array
arr.shift(); // deletes 1st value in array

arr.splice(1); // deletes 2 value i.e; 1st indexed value
arr.splice(1,3); // deletes 2nd number i.e; 2 i.e; 1st indexed values...
                // till 3rd place it will delete i.e; till 4 value it will delete.